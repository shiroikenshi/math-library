# math-library-project/math_library/functions.py

import math

def sin(angle):
    """Retorna o seno de um 창ngulo em radianos."""
    return math.sin(angle)

def cos(angle):
    """Retorna o cosseno de um 창ngulo em radianos."""
    return math.cos(angle)

def tan(angle):
    """Retorna a tangente de um 창ngulo em radianos."""
    return math.tan(angle)

def log(x, base=math.e):
    """Retorna o logaritmo de x na base especificada (por padr찾o, base e)."""
    return math.log(x, base)

def exp(x):
    """Retorna o exponencial de x."""
    return math.exp(x)